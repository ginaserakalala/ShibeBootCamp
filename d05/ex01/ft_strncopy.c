char *ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;

	i = 0;
	while (src[i] != '\0' || i < n)
	{
		++i;
		dest[i] = src[i];
	}
	n = 0;
	while(i < n)
	{
		++i;
		dest = '\0';
	}
	return dest ;
}
