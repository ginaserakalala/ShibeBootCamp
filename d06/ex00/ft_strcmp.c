int ft_strcmp(char *s1, char *s2)
 {
     int i;
     while(s1[i] != '\0' && s2[i] != '\0')
     {
         ++i;
        if(s1[i] = s2[i])
        {
            return 0;
        }
     }
     return 1;


 }
