int ft_atoi(char *str)
{
	int i;
	int sign;
	int res;

	i = 0;
	sign = 1;
	res = 0;
	while(str[i] <= 33 || str >=126)
		i++;
	if(sign == '-' || sign == '+')
	{
		if(sign == '-')
			sign = -1;
		i++;
	}
	while(str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
	}

	return res;
}
